SELECT
	CASE WHEN contract_type = 1 THEN 'SLA' ELSE 'OLA' END AS 'TIPO DE CONTRATO',
	A.NAME AS 'NOMBRE DE SLA',
	B.NAME AS 'SERVICIO ASOCIADO',
	A.FL_INT_TIMES_AT AS 'TIEMPO DE ATENCIÓN(INCI)',
	A.FL_INT_TIMES_SO AS 'TIEMPO DE SOLUCIÓN(INCI)',
	A.fl_int_times_at3 AS 'TIEMPO DE ATENCIÓN(SOLI)',
	A.fl_int_times_so3 AS 'TIEMPO DE SOLUCIÓN(SOLI)',
	FORMAT(A.DATE_START, 'dd/MM/yyyy HH:mm:ss') AS 'FECHA DE CREACIÓN',
	FORMAT(A.DATE_FINISH, 'dd/MM/yyyy HH:mm:ss') AS 'FECHA DE FINALIZACIÓN'
FROM 
	ASDK_SLA AS A
INNER JOIN 
	ASDK_SERVICE AS B ON A.FL_INT_SERVICE_ID = B.FL_INT_SERVICE_ID
ORDER BY 
	'NOMBRE DE SLA' ASC;